private fun accentPathsFor(hardwareColor: HardwareColor): List<String> = when (hardwareColor) {
    HardwareColor.TRANSPARENT -> MOCKUP_ACCENT_PATHS_TRANSPARENT
    HardwareColor.WHITE, HardwareColor.BLACK, is HardwareColor.Unrecognized -> MOCKUP_ACCENT_PATHS_STANDARD
}

@Composable
private fun rememberMockupAccentOverlayIcon(fillColor: Color, hardwareColor: HardwareColor): ImageVector {
    return remember(fillColor, hardwareColor) {
        val builder = ImageVector.Builder(
            defaultWidth = FLIPPER_DEFAULT_WIDTH.dp,
            defaultHeight = FLIPPER_DEFAULT_HEIGHT.dp,
            viewportWidth = FLIPPER_DEFAULT_WIDTH,
            viewportHeight = FLIPPER_DEFAULT_HEIGHT
        )
        accentPathsFor(hardwareColor).forEach { pathData ->
            builder.addPath(
                pathData = PathParser().parsePathString(pathData).toNodes(),
                fill = SolidColor(fillColor)
            )
        }
        builder.build()
    }
}

@Composable
internal fun MockupAccentOverlay(accentColor: Color, hardwareColor: HardwareColor, modifier: Modifier = Modifier) {
    Image(
        modifier = modifier.fillMaxSize(),
        painter = rememberVectorPainter(rememberMockupAccentOverlayIcon(accentColor, hardwareColor)),
        contentDescription = null
    )
}
